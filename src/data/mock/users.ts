// Helper function to get user by pubkey
export function getUserByPubkey(pubkey: string): MockUser | undefined {
  return mockUsers.find(u => u.pubkey === pubkey);
}

// Helper function to get user by username
export function getUserByUsername(username: string): MockUser | undefined {
  return mockUsers.find(u => u.username.toLowerCase() === username.toLowerCase());
}

// Get verified users
export function getVerifiedUsers(): MockUser[] {
  return mockUsers.filter(u => u.isVerified);
}

// Get users by badge
export function getUsersByBadge(badge: string): MockUser[] {
  return mockUsers.filter(u => u.badges?.includes(badge));
}

// Get random users
export function getRandomUsers(count: number): MockUser[] {
  const shuffled = [...mockUsers].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
}

